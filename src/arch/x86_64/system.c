inline uint8_t inb(uint16_t port)
{
    uint8_t ret;
    asm volatile ( "inb %1, %0"
                   : "=a"(ret)
                   : "Nd"(port) );
    return ret;
}

char to_lower(char a)
{
    if ((a >= 65) && (a <= 90))
        a = a + 32; 
    return a;  
}

char to_upper(char a)
{
    if ((a >= 97) && (a <= 122))
        a = a - 32; 
    return a;  
}

